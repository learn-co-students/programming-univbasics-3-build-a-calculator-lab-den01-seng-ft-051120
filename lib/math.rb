def addition(num1, num2)
  added = num1 + num2
  return added
end

def subtraction(num1, num2)
  subtracted = num1 - num2
  return subtracted
end

def division(num1, num2)
  divided = num1 / num2
  return divided
end

def multiplication(num1, num2)
  multiplied = num1 * num2
  return multiplied
end

def modulo(num1, num2)
  remainder = num1 % num2
  return remainder
end

def square_root(num)
  root = Math.sqrt(num)
  return root
end